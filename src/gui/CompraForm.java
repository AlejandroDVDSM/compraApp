    /**
     * Método que se encarga de mostrar en una lista los productos puestos en venta
     */
    void mostrarProductos() {
        ListaVenta.removeAll();
        for(int i = 0; i < producto.listaProducto.size(); i++) {
           ListaVenta.add(producto.listaProducto.get(i).getNombre() + "[" + producto.listaProducto.get(i).getPrecio() + " €]");
        }
    }
    
    /**
     * Método que se encarga de mostrar en una lista los detalles del producto puesto en venta
     * 
     * @param p: producto que contendrá los detalles del producto puesto en venta
     */
    void mostrarDetallesProductos(Producto p) {
        ListaDetallesProducto.removeAll();
        ListaDetallesProducto.add("Nombre del producto: " + p.getNombre());
        ListaDetallesProducto.add("Precio del producto: " + p.getPrecio() + " €");
        ListaDetallesProducto.add("Descripción del producto: " + p.getDescripcion());
        ListaDetallesProducto.add("¿El vendedor realiza envíos? " + p.envios());
        ListaDetallesProducto.add("El producto se encuentra: " + p.estadoProducto());
    }
    
    /**
     * Método encargado de mostrar en una lista los chats abiertos
     */
    void mostrarChat() {
        ListaChat.removeAll();
        for(int i = 0; i < chat.conver.size(); i++) {
            ListaChat.add(chat.conver.get(i).getNombreUsuarioInteresado());
        }
    }
    
    /**
     * Método encargado de mostrar en una lista los productos comprados
     */
    void mostrarComprados() {
        ListaComprados.removeAll();
        for(int i = 0; i < producto.productosComprados.size(); i++) {
            ListaComprados.add(producto.productosComprados.get(i));
        }
    }
        
    /**
     * Método que se encarga de buscar un número aleatorio de productos en el buscador de la página "Inicio"
     */
    void buscarVarios() {
        int n = (int) (Math.random() * 10); //Cabe la posibilidad de que sea 0
        if(n == 0) {
            ListaCoincidencias.add("No se ha encontrado el producto buscado...");
        } else {
            for(int i = 0; i < n; i++) {
                ListaCoincidencias.add(TFbuscar.getText() + "[" + Math.random() * 115 + "€]", i);
            }            
        }
    }
    
