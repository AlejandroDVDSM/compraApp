    /**
     * ACCIÓN DEL BOTÓN "VENDER PRODUCTO" PARA SUBIR UN PRODUCTO
     */
    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        
        if(RBsiEnvio.isSelected()) {
            if(RBnuevo.isSelected()) {
                producto.añadirProducto(TFnombreProducto.getText(), Double.parseDouble(TFprecio.getText()), TAdescripcion.getText(), true, 0);
            } else if(RBusado.isSelected()) {
                producto.añadirProducto(TFnombreProducto.getText(), Double.parseDouble(TFprecio.getText()), TAdescripcion.getText(), true, 1);
            } else if(RBdesperfecto.isSelected()) {
                producto.añadirProducto(TFnombreProducto.getText(), Double.parseDouble(TFprecio.getText()), TAdescripcion.getText(), true, 2);
            }
        } else if(RBnoEnvio.isSelected()){
            if(RBnuevo.isSelected()) {
                producto.añadirProducto(TFnombreProducto.getText(), Double.parseDouble(TFprecio.getText()), TAdescripcion.getText(), false, 0);
            } else if(RBusado.isSelected()) {
                producto.añadirProducto(TFnombreProducto.getText(), Double.parseDouble(TFprecio.getText()), TAdescripcion.getText(), false, 1);
            } else if(RBdesperfecto.isSelected()) {
                producto.añadirProducto(TFnombreProducto.getText(), Double.parseDouble(TFprecio.getText()), TAdescripcion.getText(), false, 2);
            }
        }
        
        mostrarProductos();
        limpiarTF();
    }//GEN-LAST:event_btnVenderActionPerformed

